def classify_subject(name: str) -> str:
    """判断科目属于哪张报表。"""
    canonical = to_canonical(name)
    return STMT_CATEGORY.get(canonical, "资产负债表")


def to_canonical(name: str) -> str:
    """将任意科目名称转换为标准名称。

    如果找不到对应标准名，返回原名称的归一化版本。
    """
    name_clean = name.strip().replace(" ", "").replace("　", "")
    # 全角转半角
    chars = []
    for c in name_clean:
        code = ord(c)
        if 0xFF01 <= code <= 0xFF5E:
            chars.append(chr(code - 0xFEE0))
        else:
            chars.append(c)
    name_clean = "".join(chars)

    # 查找别名映射
    for canonical, aliases in SUBJECT_ALIAS.items():
        for alias in aliases:
            alias_clean = alias.strip().replace(" ", "").replace("　", "")
            if name_clean == alias_clean:
                return canonical

    return name_clean


def get_canonical_set(names: list[str]) -> dict[str, list[str]]:
    """将一组科目名称归入标准名，返回 {标准名: [原始名, ...]}。

    用于跨年度合并时识别异名同质科目。
    """
    result: dict[str, list[str]] = {}
    for name in names:
        if not name:
            continue
        canonical = to_canonical(name)
        if canonical not in result:
            result[canonical] = []
        result[canonical].append(name)
    return result
